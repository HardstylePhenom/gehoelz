package gehoelz;

public class Strauch {
	private boolean istGiftig;
	
	public Strauch(boolean istGiftig) {
		super();
		this.istGiftig = istGiftig;
	}

	public boolean isIstGiftig() {
		return istGiftig;
	}

	public void setIstGiftig(boolean istGiftig) {
		this.istGiftig = istGiftig;
	}
}
